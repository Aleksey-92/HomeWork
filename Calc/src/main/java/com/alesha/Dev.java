package com.alesha;


public class Dev {
    public static int summ(int a,int b) {
        int i = a + b;
        return i;}
    public static int sub(int a,int b) {
        final int l = a - b;
        return l;
    }
    public static int mult(int a,int b) {
        final int j = a * b;
        return j;
    }
    public static int div(int a,int b) {
        final int h = a / b;
        return h;
    }

}
